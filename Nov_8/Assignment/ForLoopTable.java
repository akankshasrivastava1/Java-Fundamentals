package Nov_8.Assignment;

public class ForLoopTable {
    public static void main (String[] args)
    {
    System.out.println("Print 2's table using For loop: \n");
    
    for(int k=2; k <=20; k=k+2)
    {
        System.out.println(k);
    }

    System.out.println("Print 2's table using For loop in Reverse Order: \n");
    for (int b =20; b >= 2; b = b-2)
    {
      System.out.println(b);
    }               
   } 
    
}
