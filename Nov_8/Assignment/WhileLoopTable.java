package Nov_8.Assignment;

public class WhileLoopTable {
    public static void main (String[] args)
    {
    System.out.println("Print 2's table using while loop");
    int k =  2;
    while(k <=20)
    {
        System.out.println(k);
        k= k+2;
    }

    System.out.println("Print 2's table using while loop in Reverse Order");
    int b = 20;
    while (b >= 2)
    {
      System.out.println(b);
      b = b - 2;
    }               
   } 
}
